#Stages of Life

age = -2

if age < 0:
    print("Not a valid age")
elif age < 2:
    print ("You are a baby")
elif age < 4:
    print ("You are a toddler")
elif age < 13:
    print ("You are a kid")
elif age < 20:
    print ("You are a teenager")
elif age < 65:
    print ("You are a adult")
else:
    print ("You are a elder")
