# Buffet Tuples

buffet = ("burger", "pizza", "spaghetti", "risotto", "fries")

print("The items in the buffet are:")
for food in buffet:
    print(food)

buffet = ("burger", "pizza", "onion rings", "nachos", "fries")

print("The items in the buffet after the menu change are:")
for food in buffet:
    print(food)
